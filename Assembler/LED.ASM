	MOVC A,@A+DPTR
	MOV P1,A
	INC R0						;BH2
;-------------------------------------------
	mov a,r0
	anl a,#0fh
	cjne a,#00h,l1
	mov a,#10h
	add a,r0
	mov r0,a
	clr c						;!!!!!
l1:			MOV DPL,R6					;XPA/XPB
	MOV DPH,R7
	MOV A,R1					;L0----L7
	CPL A
	MOVX @DPTR,A
	mov r3,#80h
	djnz r3,$
	CALL OFFLED
	MOV A,R1
	RLC A
	MOV R1,A
	JNC DISP1
	JB 00H,EXIT
;----------------------------------
	SETB 00H
	CLR C
	MOV R1,#01H
	MOV R6,#29H
	MOV R7,#0FFH
		AJMP DISP1
EXIT:		MOV R6,#28H
	MOV R7,#0ffH
	CLR 00H
	POP 00H
	RET
;--------------------------------------------------------------------
DEL1:		MOV R2,#05H
DELY:		PUSH 02H
DEL2:		PUSH 02H
DEL3:		PUSH 02H
DEL4:		DJNZ R2,DEL4
	CALL DISPW
	POP 02H
	DJNZ R2,DEL3
	POP 02H
	DJNZ R2,DEL2
	POP 02H
	DJNZ R2,DELY
	RET
;---------------------------------------------------------
OFFLED:   MOV DPTR,#XPA
	MOV A,#0FFH
	MOVX @DPTR,A
	MOV DPTR,#XPB
	MOVX @DPTR,A	
	RET
