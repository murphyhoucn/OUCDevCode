#include <stdio.h>

int main()
{
	int a;
	a = 123;
	printf("%d\n", a);

	int b;
	b = 123.456;
	printf("%d\n", b);

	float c;
	double d;

	c = 3;
	d = 4;
	printf("%f\n", c);
	printf("%f\n", d);

	float e;
	e = 123.456789123456;
	printf("%f\n", e);

	int f;
	f = 'A';
	printf("%d\n", f);

	char g;
	g = 1234;
	printf("%d\n", g);
	printf("%c\n", g);

	return 0;
}